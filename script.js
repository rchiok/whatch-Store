function validateEmail() {
    var emailInput = document.getElementById('email'); // يتم الحصول على عنصر الإدخال بواسطة الحصول على العنصر الذي يحمل العنوان 'email'
    var email = emailInput.value.trim(); // يتم الحصول على قيمة البريد الإلكتروني المدخلة وإزالة أي فراغات زائدة من البداية والنهاية

    var emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/; // يتم تعريف نمط التطابق للتحقق من صحة عنوان البريد الإلكتروني

    if (emailRegex.test(email)) { // إذا كانت القيمة المدخلة للبريد الإلكتروني تطابق نمط التطابق
        alert('Email is valid.'); // يتم عرض رسالة تأكيد بأن عنوان البريد الإلكتروني صحيح
        emailInput.value = ''; // يتم مسح قيمة البريد الإلكتروني المدخلة من الحقل
        return true; // تُرجع قيمة صحيحة للتأكيد على صحة البريد الإلكتروني
    } else {
        alert('Please enter a valid email.'); // يتم عرض رسالة تنبيه بأن عنوان البريد الإلكتروني غير صحيح
        return false; // تُرجع قيمة غير صحيحة للإشارة إلى عدم صحة البريد الإلكتروني
    }
}